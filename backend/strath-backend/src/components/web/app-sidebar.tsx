interface AppSidebarProps {
  user: {
    id: string;
    name: string;
    email: string;
    image?: string | null;
  };
  profile: {
    profilePhoto?: string | null;
    firstName?: string | null;
  } | null;
}

const navItems = [
  { href: "/app/discover", label: "Discover", icon: CompassIcon },
  { href: "/app/matches", label: "Matches", icon: HeartIcon },
  { href: "/app/messages", label: "Messages", icon: ChatIcon },
  { href: "/app/profile", label: "Profile", icon: UserIcon },
];

export function AppSidebar({ user, profile }: AppSidebarProps) {
  const pathname = usePathname();
  const router = useRouter();

  const handleSignOut = async () => {
    await authClient.signOut();
    router.push("/login");
  };

  const displayName = profile?.firstName || user.name || "User";
  const avatarUrl = profile?.profilePhoto || user.image;

  return (
    <Sidebar className="border-r border-white/10 bg-[#1a1a2e]">
      <SidebarHeader className="border-b border-white/10 p-4">
        <Link href="/app/discover" className="flex items-center gap-3">
          <div className="w-10 h-10 bg-gradient-to-br from-pink-500 via-rose-500 to-purple-600 rounded-xl flex items-center justify-center shadow-lg">
            <Image
              src="/logo.png"
              alt="Strathspace"
              width={24}
              height={24}
              className="object-contain"
            />
          </div>
          <span className="text-xl font-bold text-white">Strathspace</span>
        </Link>
      </SidebarHeader>

      <SidebarContent className="p-2">
        <SidebarGroup>
          <SidebarGroupContent>
            <SidebarMenu>
              {navItems.map((item) => {
                const isActive = pathname === item.href || pathname.startsWith(item.href + "/");
                return (
                  <SidebarMenuItem key={item.href}>
                    <SidebarMenuButton
                      asChild
                      isActive={isActive}
                      className={`h-12 text-base ${
                        isActive 
                          ? "bg-gradient-to-r from-pink-500/20 to-rose-500/20 text-pink-400 border-l-2 border-pink-500" 
                          : "text-gray-400 hover:text-white hover:bg-white/5"
                      }`}
                    >
                      <Link href={item.href}>
                        <item.icon />
                        <span>{item.label}</span>
                      </Link>
                    </SidebarMenuButton>
                  </SidebarMenuItem>
                );
              })}
            </SidebarMenu>
          </SidebarGroupContent>
        </SidebarGroup>
      </SidebarContent>

      <SidebarFooter className="border-t border-white/10 p-2">
        <SidebarMenu>
          <SidebarMenuItem>
            <DropdownMenu>
              <DropdownMenuTrigger asChild>
                <SidebarMenuButton className="h-14 text-white hover:bg-white/5">
                  <Avatar className="h-8 w-8">
                    <AvatarImage src={avatarUrl || undefined} />
                    <AvatarFallback className="bg-gradient-to-br from-pink-500 to-rose-500 text-white">
                      {displayName.charAt(0).toUpperCase()}
                    </AvatarFallback>
                  </Avatar>
                  <div className="flex flex-col items-start text-left">
                    <span className="text-sm font-medium">{displayName}</span>
                    <span className="text-xs text-gray-500 truncate max-w-[120px]">
                      {user.email}
                    </span>
                  </div>
                  <ChevronUpIcon />
                </SidebarMenuButton>
              </DropdownMenuTrigger>
              <DropdownMenuContent
                side="top"
                align="start"
                className="w-56 bg-[#1a1a2e] border-white/10"
              >
                <DropdownMenuItem asChild className="text-gray-300 hover:text-white focus:text-white focus:bg-white/10">
                  <Link href="/app/settings">
                    <SettingsIcon />
                    <span className="ml-2">Settings</span>
                  </Link>
                </DropdownMenuItem>
                <DropdownMenuSeparator className="bg-white/10" />
                <DropdownMenuItem 
                  onClick={handleSignOut}
                  className="text-red-400 hover:text-red-300 focus:text-red-300 focus:bg-red-500/10"
                >
                  <LogoutIcon />
                  <span className="ml-2">Sign out</span>
                </DropdownMenuItem>
              </DropdownMenuContent>
            </DropdownMenu>
          </SidebarMenuItem>
        </SidebarMenu>
      </SidebarFooter>
    </Sidebar>
  );
}
